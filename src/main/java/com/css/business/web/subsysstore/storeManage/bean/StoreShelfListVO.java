package com.css.business.web.subsysstore.storeManage.bean;

public class StoreShelfListVO {
  private int id;
  private String shelfname;
  private String floor;
  private String columns;
  private String capacityeach;
  private String remain;
  private String packagetype;
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getShelfname() {
	return shelfname;
}
public void setShelfname(String shelfname) {
	this.shelfname = shelfname;
}
public String getFloor() {
	return floor;
}
public void setFloor(String floor) {
	this.floor = floor;
}

public String getColumns() {
	return columns;
}
public void setColumns(String columns) {
	this.columns = columns;
}
public String getCapacityeach() {
	return capacityeach;
}
public void setCapacityeach(String capacityeach) {
	this.capacityeach = capacityeach;
}
public String getRemain() {
	return remain;
}
public void setRemain(String remain) {
	this.remain = remain;
}
public String getPackagetype() {
	return packagetype;
}
public void setPackagetype(String packagetype) {
	this.packagetype = packagetype;
}
  
}
