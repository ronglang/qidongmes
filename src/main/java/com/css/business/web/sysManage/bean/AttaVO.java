package com.css.business.web.sysManage.bean;
/**
 * 附件VO 只取关键数据 for app
 * @author leitao
 */
public class AttaVO {

	private String url;
	private String type;
	private String name;
	
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
}
