 package com.css.common.util;
 
 public class FileAttr
 {
   private String absPath;
   private String relatePath;
   private Boolean isDirectory;
   private String upabsPath;
   private String uprealtePath;
   
   public String getAbsPath()
   {
     return this.absPath;
   }
   
   public void setAbsPath(String absPath)
   {
     this.absPath = absPath;
   }
   
   public String getRelatePath()
   {
     return this.relatePath;
   }
   
   public void setRelatePath(String relatePath)
   {
     this.relatePath = relatePath;
   }
   
   public Boolean getIsDirectory()
   {
     return this.isDirectory;
   }
   
   public void setIsDirectory(Boolean isDirectory)
   {
     this.isDirectory = isDirectory;
   }
   
   public String getUpabsPath()
   {
     return this.upabsPath;
   }
   
   public void setUpabsPath(String upabsPath)
   {
     this.upabsPath = upabsPath;
   }
   
   public String getUprealtePath()
   {
     return this.uprealtePath;
   }
   
   public void setUprealtePath(String uprealtePath)
   {
     this.uprealtePath = uprealtePath;
   }
 }






